float Time::GetDelta() {
	static Uint32 s_ticksLast = SDL_GetTicks();
	Uint32 ticks = SDL_GetTicks();
	double dt = 0.001 * (ticks - s_ticksLast);
	s_ticksLast = ticks;

	return static_cast<float>(dt);
}

